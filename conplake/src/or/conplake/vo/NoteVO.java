package or.conplake.vo;

public class NoteVO {
	private int note_num, note_from, note_to;
	private String note_cont, note_date,
	fromname, toname;
	
	public String getFromname() {
		return fromname;
	}
	public void setFromname(String fromname) {
		this.fromname = fromname;
	}
	public String getToname() {
		return toname;
	}
	public void setToname(String toname) {
		this.toname = toname;
	}
	public int getNote_num() {
		return note_num;
	}
	public void setNote_num(int note_num) {
		this.note_num = note_num;
	}
	public int getNote_from() {
		return note_from;
	}
	public void setNote_from(int note_from) {
		this.note_from = note_from;
	}
	public int getNote_to() {
		return note_to;
	}
	public void setNote_to(int note_to) {
		this.note_to = note_to;
	}
	public String getNote_cont() {
		return note_cont;
	}
	public void setNote_cont(String note_cont) {
		this.note_cont = note_cont;
	}
	public String getNote_date() {
		return note_date;
	}
	public void setNote_date(String note_date) {
		this.note_date = note_date;
	}
	
	

}
