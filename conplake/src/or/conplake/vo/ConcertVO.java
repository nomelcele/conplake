package or.conplake.vo;

public class ConcertVO {
	private int con_num, con_venue, con_artist;
	private String con_name, con_poster, con_startdate, con_enddate,
	con_link, con_detailimg, con_price, con_tel;
	
	public int getCon_num() {
		return con_num;
	}
	public void setCon_num(int con_num) {
		this.con_num = con_num;
	}
	public int getCon_venue() {
		return con_venue;
	}
	public void setCon_venue(int con_venue) {
		this.con_venue = con_venue;
	}
	public int getCon_artist() {
		return con_artist;
	}
	public void setCon_artist(int con_artist) {
		this.con_artist = con_artist;
	}
	public String getCon_name() {
		return con_name;
	}
	public void setCon_name(String con_name) {
		this.con_name = con_name;
	}
	public String getCon_poster() {
		return con_poster;
	}
	public void setCon_poster(String con_poster) {
		this.con_poster = con_poster;
	}
	public String getCon_startdate() {
		return con_startdate;
	}
	public void setCon_startdate(String con_startdate) {
		this.con_startdate = con_startdate;
	}
	public String getCon_enddate() {
		return con_enddate;
	}
	public void setCon_enddate(String con_enddate) {
		this.con_enddate = con_enddate;
	}
	public String getCon_link() {
		return con_link;
	}
	public void setCon_link(String con_link) {
		this.con_link = con_link;
	}
	public String getCon_detailimg() {
		return con_detailimg;
	}
	public void setCon_detailimg(String con_detailimg) {
		this.con_detailimg = con_detailimg;
	}
	public String getCon_price() {
		return con_price;
	}
	public void setCon_price(String con_price) {
		this.con_price = con_price;
	}
	public String getCon_tel() {
		return con_tel;
	}
	public void setCon_tel(String con_tel) {
		this.con_tel = con_tel;
	}
	
	
}
