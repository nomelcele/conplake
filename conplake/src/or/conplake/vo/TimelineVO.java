package or.conplake.vo;

public class TimelineVO {
	private int tl_num, tl_author, tl_concert;
	private String tl_cont, tl_date;
	
	public int getTl_num() {
		return tl_num;
	}
	public void setTl_num(int tl_num) {
		this.tl_num = tl_num;
	}
	public int getTl_author() {
		return tl_author;
	}
	public void setTl_author(int tl_author) {
		this.tl_author = tl_author;
	}
	public int getTl_concert() {
		return tl_concert;
	}
	public void setTl_concert(int tl_concert) {
		this.tl_concert = tl_concert;
	}
	public String getTl_cont() {
		return tl_cont;
	}
	public void setTl_cont(String tl_cont) {
		this.tl_cont = tl_cont;
	}
	public String getTl_date() {
		return tl_date;
	}
	public void setTl_date(String tl_date) {
		this.tl_date = tl_date;
	}
	
	

}
